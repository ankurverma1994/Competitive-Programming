    @SuppressWarnings("Main Logic")
    void solve() {
        n = ii();
        m = ii();
        k = ii();
        c = iia(n);
        cost = iim(n, m);
        init();
        long ans = solve(0, -1, 0);
        out.println(ans >= inf ? "-1" : ans);
    }

    void init() {
        dp = new long[n + 10][m + 10][k + 10];
        for (int i = 0; i < dp.length; i++)
            for (int j = 0; j < dp[i].length; j++)
                Arrays.fill(dp[i][j], -1);
    }

    int n, m, k, c[], cost[][];
    long dp[][][];

    long solve(int i, int last, int parts) {
//        out.println(i + " " + last + " " + parts);
        if (parts > k) return inf;
        if (i >= n) {
            return parts == k ? 0 : inf;
        }
        if (last != -1 && dp[i][last][parts] != -1)
            return dp[i][last][parts];
        long ans = inf;
        if (c[i] == 0) {
            for (int j = 1; j <= m; j++) {
                ans = Math.min(ans, cost[i][j - 1] + solve(i + 1, j, parts + (last == j ? 0 : 1)));
            }
        } else {
            ans = Math.min(ans, solve(i + 1, c[i], parts + ((last == c[i]) ? 0 : 1)));
        }
        if (last == -1) return ans;
        return dp[i][last][parts] = ans;
    }


