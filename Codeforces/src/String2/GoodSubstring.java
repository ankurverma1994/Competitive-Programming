    @SuppressWarnings("Main Logic")
    void solve() {
        char s[] = is().toCharArray();
        char c[] = is().toCharArray();
        int k = ii();
        boolean bad[] = new boolean[26];
        for (int i = 0; i < c.length; i++) bad[i] = c[i] == '0';
        HashSet<Pair> set = new HashSet<>();
        for (int i = 0; i < s.length; i++) {
            int count = 0;
            long hash1 = 0, hash2 = 0;
            for (int j = i; j < s.length; j++) {
                if (bad[s[j] - 'a']) count++;
                if (count > k) break;
                hash1 = hash1 * magic + s[j];
                hash2 = hash2 * magic + s[j];
                hash1 %= mod1;
                hash2 %= mod2;
                set.add(new Pair(hash1, hash2));
            }
        }
        out.println(set.size());
    }

    static final int magic = 31;
    static final int mod1 = (int) 1e9 + 7;
    static final int mod2 = (int) 1e9 + 9;

    static class Pair {
        long x, y;

        Pair(long a, long b) {
            x = a;
            y = b;
        }

        @Override
        public boolean equals(Object o) {
            Pair obj = (Pair) o;
            return obj.x == x && obj.y == y;
        }

        @Override
        public int hashCode() {
            return new Long(x).hashCode() * 31 + new Long(y).hashCode();
        }
    }

