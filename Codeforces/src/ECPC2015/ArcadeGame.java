        fact[0] = fact[1] = 1;
        for (int i = 2; i <= 10; i++) fact[i] = i * fact[i - 1];
        int a = ii();
        int n = String.valueOf(a).length();
        num = a;
        int x[] = new int[n];
        int i = 0;
        while (a > 0) {
            x[i] = a % 10;
            a /= 10;
            i++;
        }
        generatePermutations(x, 0, n - 1);
