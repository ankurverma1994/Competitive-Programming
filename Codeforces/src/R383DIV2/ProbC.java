    void solve() {
        int n = ii();
        int from[] = new int[n];
        int to[] = new int[n];
        for (int i = 0; i < n; i++) {
            from[i] = i;
            to[i] = ii() - 1;
        }
        g = packD(n, from, to, n);
//        out.println(Arrays.deepToString(g));
        long ret = 1;
        for (int i = 0; i < n; i++) {
            visit = new boolean[n];
            ans = 0;
            dfs(i, i, 0);
            if (ans == -1) {
                out.println("-1");
                return;
            }
//            out.println(ans);
            if (ans % 2 == 0) ans /= 2;
            ret = lcm(ret, ans);
        }
        out.println(ret);
    }

    long lcm(long a, long b) {
        return a * b / gcd(a, b);
    }

    long gcd(long a, long b) {
        while (b > 0) {
            long c = a;
            a = b;
            b = c % b;
        }
        return a;
    }

    boolean visit[];
    int ans;

    void dfs(int curr, int start, int len) {
//        out.println(curr + " " + len);
        if (visit[curr]) {
            if (curr != start) {
                ans = -1;
            } else {
                ans = len;
            }
            return;
        }
        visit[curr] = true;
        for (int next : g[curr]) {
            dfs(next, start, len + 1);
        }
    }

