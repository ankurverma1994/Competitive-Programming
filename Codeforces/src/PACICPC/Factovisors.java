    void solve() {
        int n = ii(), m = ii();
        if (n >= m) {
            out.println(m + " divides " + n + "!");
            return;
        }
        int fac[] = facs(m);
        int count = 1, p = fac[0];
        for (int i = 1; i < fac.length; i++) {
            if (fac[i] == p) count++;
            else {
                if (HighestPower(n, p) < count) {
                    out.println(m + "does not divides " + n + "!");
                    return;
                }
                count = 1;
                p = fac[i];
            }
        }
        if (HighestPower(n, p) < count) {
            out.println(m + "does not divides " + n + "!");
            return;
        }
        out.println(m + " divides " + n + "!");
    }

    int HighestPower(int n, int p) {
        int count = 0;
        while (n / p > 0) {
            count += n / p;
            p *= p;
        }
        return count;
    }

    public static int[] facs(int n) {
        int ret[] = new int[1000];
        int j = 0;
        for (int i = 2; i * i <= n; i++) {
            while (n % i == 0) {
                ret[j++] = i;
                n /= i;
            }
        }
        if (n > 1) ret[j++] = n;
        return Arrays.copyOf(ret, j);
    }

