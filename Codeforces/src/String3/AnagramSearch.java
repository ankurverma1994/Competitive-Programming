    void solve() {
        char s[] = is().toCharArray(), p[] = is().toCharArray();
        int f[] = new int[26];
        int window[] = new int[26];
        int ls = s.length, lp = p.length;
        if (lp > ls) {
            out.println("0");
            return;
        }
        int ans = 0;
        for (int i = 0; i < lp; i++) {
            if (s[i] != '?')
                window[s[i] - 'a']++;
            f[p[i] - 'a']++;
        }

        if (good(f, window)) ans++;
        for (int i = lp; i < ls; i++) {
            if (s[i - lp] != '?') {
                window[s[i-lp] - 'a']--;
            }
            if (s[i] != '?') window[s[i] - 'a']++;
            if (good(f, window)) ans++;
        }
        out.println(ans);
    }

    boolean good(int f[], int window[]) {
        for (int i = 0; i < 26; i++) {
            if (window[i] > f[i])
                return false;
        }
        return true;
    }


