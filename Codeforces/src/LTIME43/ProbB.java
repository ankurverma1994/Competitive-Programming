    void solve() {
        int n = ii(), a[] = iia(n);
        TreeMap<Integer, Integer> map = new TreeMap<>();
        for (int i = 0; i < n - 1; i++) {
            int prev = a[i], next = a[i + 1] + 1;
            if (!map.containsKey(prev)) map.put(prev, 0);
            if (!map.containsKey(next)) map.put(next, 0);
            map.put(prev, map.get(prev) + 1);
            map.put(next, map.get(next) - 1);
        }
        if (!map.containsKey(a[n - 1])) map.put(a[n - 1], 0);
        map.put(a[n - 1], map.get(a[n - 1]) + 1);
        long cum[] = new long[n];
        int i = 0;
        long max = 0;
        int maxIndex = -1;
        for (Map.Entry<Integer, Integer> x : map.entrySet()) {
//            out.println(x.getKey() + " " + x.getValue());
            if (i == 0) cum[i] = x.getValue();
            else cum[i] += cum[i - 1] + x.getValue();
            if (cum[i] >= max) {
                max = cum[i];
                maxIndex = i;
            }
            i++;
        }
        i = 0;
        for (Map.Entry<Integer, Integer> x : map.entrySet()) {
            if (i == maxIndex) {
                out.println(x.getKey());
                return;
            }
            i++;
        }
    }


