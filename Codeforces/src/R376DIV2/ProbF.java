    void solve() {
        int n = ii(), a[] = iia(n);
        int max = 0;
        for (int i : a) max = Math.max(i, max);
        int f[] = new int[max + 1];
        for (int i : a) f[i]++;
        long cumf[] = new long[max + 2];
        for (int i = 1; i <= max; i++) cumf[i] += (cumf[i - 1] + f[i]);

        long ans = 0;
        for (int num = 1; num <= max; num++) {
            if (f[num] == 0) continue;
            long sum = 0;
            for (int next = num; next <= max; next += num) {
                int low = next;
                int high = Math.min(next + num - 1, max);
                sum = sum + low * (cumf[high] - cumf[low - 1]);
            }
            ans = Math.max(ans, sum);
        }
        out.println(ans);
    }


