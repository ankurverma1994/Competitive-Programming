    void solve() {
        long l = il(), r = il();
        int gcd = ii(), lcm = ii();
        if (lcm % gcd != 0) {
            out.println(0);
            return;
        }
        int num = lcm / gcd;
        long fac[] = new long[20];
        int index = 0;
        long a = 1;
        while (num % 2 == 0) {
            num /= 2;
            a *= 2;
        }
        if (a * gcd > r) {
            out.println(0);
            return;
        }
        if (a != 1) {
            fac[index++] = a;
        }
        for (int i = 3; i <= Math.sqrt(num); i += 2) {
            a = 1;
            while (num % i == 0) {
                num /= i;
                a *= i;
            }
            if (a * gcd > r) {
                out.println(0);
                return;
            }
            if (a != 1) {
                fac[index++] = a;
            }
        }
        if (num > 2) {
            if (num * gcd > r) {
                out.println(0);
                return;
            }
            fac[index++] = num;
        }
        num = lcm / gcd;
        Set<String> ans = new HashSet<>();
        for (int counter = 0; counter < (1 << index); counter++) {
            long x = 1;
            for (int j = 0; j < index; j++) {
                if ((counter & (1 << j)) > 0) {
                    x *= fac[j];
                }
            }
            long y = num / x;
            x *= gcd;
            y *= gcd;
            if (l <= x && x <= r && l <= y && y <= r) ans.add(x + " " + y);
        }
        out.println(ans.size());
    }

