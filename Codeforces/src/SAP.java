    int solve(int a[][], int n, int x, int y) {
        if (x == n - 1 && y == n - 1) {
            return a[x][y];
        }
        if (x < 0 || x >= n || y < 0 || y >= n)
            return Integer.MIN_VALUE / 4;
        if (dp[x][y] != -1)
            return dp[x][y];
        return dp[x][y] = a[x][y] + Math.max(solve(a, n, x + 1, y), solve(a, n, x, y + 1));
    }


