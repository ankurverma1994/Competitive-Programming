    long solve(int i, int remaining) {
        if (i >= n) {
            if (remaining == m) return 0;
            return remaining * b;
//            return 0;
        }
        long ans = inf;
        if (remaining == time[i])
            ans = Math.min(ans, solve(i + 1, m));
        // yahi par rakho
        if (remaining - time[i] > 0)
            ans = Math.min(ans, solve(i + 1, remaining - time[i]));
        // cut kar k rakho
        if (time[i] > remaining)
            ans = Math.min(ans, (time[i] - remaining) * a + solve(i + 1, m));
        // next block mei bhejo
        if (time[i] < remaining && remaining != m)
            ans = Math.min(ans, remaining * b + solve(i, m));
        return ans;
    }

