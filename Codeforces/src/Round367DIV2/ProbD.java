    void solve() {
        root = new Node();
        numberAdd(0, 1);
        for (int tc = ii(); tc > 0; tc--) {
            char c = ic();
            if (c == '+')
                numberAdd(ii(), 1);
            if (c == '-')
                numberAdd(ii(), -1);
            if (c == '?')
                out.println(maxXOR(ii()));
        }
    }

    int ALPHA = 2;
    Node root;

    class Node {
        int cnt;
        Node child[];

        Node() {
            cnt = 0;
            child = new Node[ALPHA];
        }
    }

    void numberAdd(int x, int freq) {
        Node curr = root;
        for (int i = 30; i >= 0; i--) {
            int bit = ((1 << i) & x) > 0 ? 1 : 0; // ith bit
            if (curr.child[bit] == null)
                curr.child[bit] = new Node();
            curr = curr.child[bit];
            curr.cnt += freq;
        }
    }

    int maxXOR(int x) {
        Node curr = root;
        int ans = 0;
        for (int i = 30; i >= 0; i--) {
            int bit = ((1 << i) & x) > 0 ? 1 : 0;
            int oppBit = 1 - bit;
            if (curr.child[oppBit] != null && curr.child[oppBit].cnt > 0) {
                ans += (1 << i);
                curr = curr.child[oppBit];
            } else {
                curr = curr.child[bit];
            }
        }
        return ans;
    }

