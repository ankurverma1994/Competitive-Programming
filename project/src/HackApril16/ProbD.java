    @SuppressWarnings("Main Logic")
    void solve() {
        for (int tc = ii(); tc > 0; tc--) {
            String s = is();
            ArrayList<String> substring = getAllUniqueSubset(s);
            long ans = 0;
            for (int i = 0; i < substring.size(); i++) {
                char a[] = substring.get(i).toCharArray();
                int l = a.length;
                int d = distinct(a);
                ans = (ans + modpow(l, d, mod)) % mod;
            }
            out.println(ans);
        }
    }

    // Complexity O(log n)
    public static long modpow(long base, int exp, int mod) {
        base %= mod;
        long result = 1;
        while (exp > 0) {
            if ((exp & 1) == 1)
                result = (result * base) % mod;
            base = (base * base) % mod;
            exp >>= 1;
        }
        return result;
    }

    int distinct(char s[]) {
        int f[] = new int[26];
        for (char c : s)
            f[c - 'a']++;
        int count = 0;
        for (int i : f)
            if (i >= 1)
                count++;
        return count;
    }

    public ArrayList<String> getAllUniqueSubset(String str) {
        ArrayList<String> set = new ArrayList<String>();
        for (int i = 0; i < str.length(); i++) {
            for (int j = 0; j < str.length() - i; j++) {
                String elem = str.substring(j, j + (i + 1));
                if (!set.contains(elem)) {
                    set.add(elem);
                }
            }
        }
        return set;
    }

