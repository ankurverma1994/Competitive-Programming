    @SuppressWarnings("Main Logic")
    void solve() {
        long sum = 0;
        int count = 0;
        int primes[] = gen_primes(1000000);
        long n = il();
        for (int p : primes) {
            sum += p;
            if (sum > n || sum > 12000000000L)
                break;
            if (sum == 2) continue;
            if (isPrime(sum)) {
                count++;
            }
        }
        out.println(count);
    }

    boolean isPrime(long number) {
        if (number < 2)
            return false;
        for (long i = 2; i * i <= number; i++) {
            if (number % i == 0)
                return false;
        }
        return true;
    }

    int[] gen_primes(int n) {
        boolean prime[] = new boolean[n + 1];
        int ans[] = new int[n + 1];
        int length = 0;
        Arrays.fill(prime, true);
        for (int i = 2; i * i <= n; i++) {
            if (prime[i] == true) {
                for (int j = i * 2; j <= n; j += i)
                    prime[j] = false;
            }
        }
        for (int i = 2; i <= n; i++)
            if (prime[i])
                ans[length++] = i;
        return Arrays.copyOf(ans, length);
    }

